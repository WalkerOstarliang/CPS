	public String getDzmc()
	{
		dzmc = "";
		if (ValidateHelper.isNotEmptyString(xzqhmc))
		{
			dzmc += xzqhmc;
		}
		if (ValidateHelper.isNotEmptyString(jdxzmc))
		{
			dzmc += jdxzmc;
		}
		if (ValidateHelper.isNotEmptyString(sqmc))
		{
			dzmc += sqmc;
		}
		if (ValidateHelper.isNotEmptyString(jlxmc))
		{
			dzmc += jlxmc;
		}
		if (ValidateHelper.isNotEmptyString(mph))
		{
			if(mph.endsWith("号")){
				dzmc += mph;
			}else{
				dzmc += mph + "号";
			}
		}
		if (ValidateHelper.isNotEmptyString(xqz))
		{
			dzmc += xqz;
		}
		if (ValidateHelper.isNotEmptyString(ldxz))
		{
			dzmc += ldxz;
		}
		if (ValidateHelper.isNotEmptyString(dyh))
		{
			dzmc += DictionaryCacheHellper.getSystemDictionaryValueByZdlbAndDm("ZDY_DYH", dyh);
		}
		if (ValidateHelper.isNotEmptyString(lch))
		{
			dzmc += lch;
			if (ValidateHelper.isNotEmptyString(lchz))
			{
				dzmc += DictionaryCacheHellper.getSystemDictionaryValueByZdlbAndDm("ZDY_LCHZ", lchz);
			}
		}
		if (ValidateHelper.isNotEmptyString(fjh))
		{
			dzmc += fjh;
			if (ValidateHelper.isNotEmptyString(shhz))
			{
				dzmc += DictionaryCacheHellper.getSystemDictionaryValueByZdlbAndDm("ZDY_SHHZ", shhz);
			}
		}
		return dzmc;
	}

